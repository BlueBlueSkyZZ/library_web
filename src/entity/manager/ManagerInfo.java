package entity.manager;

public class ManagerInfo {

	private String id;
	private String name;
	private String rank;
	private String manage_admin;
	private String manage_data;
	private String manage_scan;
	private String tel;
	private String address;
	private String account;
	private String password;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getRank() {
		return rank;
	}
	public void setRank(String rank) {
		this.rank = rank;
	}
	public String getManage_admin() {
		return manage_admin;
	}
	public void setManage_admin(String manage_admin) {
		this.manage_admin = manage_admin;
	}
	public String getManage_data() {
		return manage_data;
	}
	public void setManage_data(String manage_data) {
		this.manage_data = manage_data;
	}
	public String getManage_scan() {
		return manage_scan;
	}
	public void setManage_scan(String manage_scan) {
		this.manage_scan = manage_scan;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getAddress() {
		return address;
	}
	public void setAddress(String address) {
		this.address = address;
	}
	public String getAccount() {
		return account;
	}
	public void setAccount(String account) {
		this.account = account;
	}
	public String getPassword() {
		return password;
	}
	public void setPassword(String password) {
		this.password = password;
	}
	
	
}
